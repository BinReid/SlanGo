        // Инициализация при загрузке страницы
        document.addEventListener('DOMContentLoaded', function() {
            const wordsGrid = document.getElementById('words-grid');
            const wordDetails = document.getElementById('word-details');
            const closeDetails = document.getElementById('close-details');
            const searchInput = document.getElementById('search-input');
            const wordsCount = document.getElementById('words-count');
            
            const words = slangDictionary.сленговый_словарь.слова;
            
            // Обновляем счетчик слов
            wordsCount.textContent = words.length;
            
            // Функция для отображения слов
            function displayWords(filteredWords = words) {
                wordsGrid.innerHTML = '';
                
                filteredWords.forEach(word => {
                    const wordBtn = document.createElement('button');
                    wordBtn.className = 'word-btn';
                    wordBtn.textContent = word.слово;
                    wordBtn.addEventListener('click', () => showWordDetails(word));
                    wordsGrid.appendChild(wordBtn);
                });
            }
            
            // Функция для показа деталей слова
            function showWordDetails(word) {
                document.getElementById('detail-word').textContent = word.слово;
                document.getElementById('detail-meaning').textContent = word.значение;
                document.getElementById('detail-origin').textContent = word.происхождение;
                
                wordDetails.classList.add('active');
                
                // Прокрутка к деталям
                wordDetails.scrollIntoView({ behavior: 'smooth' });
            }
            
            // Функция поиска
            function handleSearch() {
                const searchTerm = searchInput.value.toLowerCase().trim();
                
                if (searchTerm === '') {
                    displayWords();
                    return;
                }
                
                const filteredWords = words.filter(word => 
                    word.слово.toLowerCase().includes(searchTerm) ||
                    word.значение.toLowerCase().includes(searchTerm) ||
                    word.происхождение.toLowerCase().includes(searchTerm)
                );
                
                displayWords(filteredWords);
            }
            
            // События
            closeDetails.addEventListener('click', () => {
                wordDetails.classList.remove('active');
            });
            
            searchInput.addEventListener('input', handleSearch);
            
            // Инициализация отображения слов
            displayWords();
            
            // Добавляем обработчик Escape для закрытия деталей
            document.addEventListener('keydown', (e) => {
                if (e.key === 'Escape') {
                    wordDetails.classList.remove('active');
                }
            });
        });